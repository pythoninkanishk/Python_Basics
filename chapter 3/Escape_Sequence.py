#TO CHANGE LINE OF WORDS IN STRING
NAME = "AUR BHAI KAISA HAI \n KYA CHAL RAHA HAI" #\n is used to create a new line in the string
print(NAME)

# TO ADD "" IN THE STRING   
NAME2 = "AUR BHAI KAISA HAI \n HAAL CHAAL BATA \"KANISHK\"" # \" is used to escape double quotes in the string

# TO ADD TAB SPACE IN THE STRING
NAME3 = "AUR BHAI KAISA HAI\tHAAL CHAAL BATA" # \t is used to add a tab space in the string
print(NAME3)

# TO ADD SINGLE QUOTE IN THE STRING
NAME4 = 'AUR BHAI KAISA HAI\nHAAL CHAAL BATA \'KANISHK\'' # \' is used to escape single quotes in the string
print(NAME4)

# TO ADD BACKSLASH IN THE STRING
NAME5 = "AUR BHAI KAISA HAI\\HAAL CHAAL BATA" # \\ is used to add a backslash in the string
print(NAME5)
print(NAME2)