# 1. Write a program using functions to find greatest of three numbers.

def Greatest_No():
    no1 = int(input("Enter Your First Number: "))
    no2 = int(input("Enter Your Second Number: "))
    no3 = int(input("Enter Your Third Number: "))
    if no1>no2 and no1>no3:
        print("First Number is the greatest number")
    elif no2>no1 and no2>no3:
        print("Second Number is the greatest number")
    elif no3>no1 and no3>no2:
        print("Third Number is the greatest number")
Greatest_No()    
